const byungmat: TemplateRenderer = ({ words }) => `
[병맛 반전스토리 작법 가이드]
다음 5개 단어를 사용하라: ${words.join(", ")}

목표:
- 사건은 얼마나 황당하든 **어조는 끝까지 다큐처럼 진지**하다.
- **논리적 인과** 위에 **비논리 요소**를 1~2개만 삽입한다(과다 금지).
- **결말 직전(5단락)**에서 반전을 발생시키고, **6단락은 과장 없이 건조하게** 사후를 기술한다.
- **존재할 것 같지만 실제로는 없는** 인물·지명·사건·물건을 1개 이상 등장시킨다(사실처럼 서술, 메타 금지).
- **설명/해설 금지**: 인물은 비논리를 인식하지 못한다. 독자가 스스로 웃도록 둔다.

구조 (총 1200자, 6단락):
(1) 평범하고 진지한 상황 시작. {단어1} → 실무 보고처럼 간결
(2) 사소한 사건 전개. {단어2} → 구체 수치/절차 1회 삽입(현실감)
(3) 사건이 복잡해짐. {단어3} → 의미 없이 장엄한 디테일 1개
(4) 절정 직전, 긴장 축적. {단어4} → 인물은 여전히 냉정
(5) **반전 발생**. {단어5}, 초반 단어 중 1개 재활용 → 반전 사실만 제시(해설 금지)
(6) **사후 처리**를 공문서 톤으로 3~4문장. 결말은 독자 상상에 맡김

문체 규칙(병맛):
- **문장 톤:** 냉정·보고서·매뉴얼 같은 어휘 1~2회 사용(예: “관련 규정에 따름”, “기재 바람”).
- **비유:** 과장은 1~2회만, 은근하게. (과잉 금지)
- **실명/브랜드/지명**은 가짜로 만들되, 실제처럼 보이는 디테일 1개 부여.
- **금지:** 메타 발언(“내가 지금 병맛을 쓰고 있다”), 반전 해설, 독자에게 직접 말 걸기.

${COMMON_BLOCK}`;
